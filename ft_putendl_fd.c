#include "libft.h"

static void	st_putchar_fd(char c, int fd)
{
	write(fd, &c, 1);
}

void	ft_putendl_fd(char *s, int fd)
{
	size_t	i;

	i = 0;
	while (s[i])
		st_putchar_fd(s[i++], fd);
	st_putchar_fd('\n', fd);
}
